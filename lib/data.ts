export interface OrderItem {
  productId: string;
  name: string;
  image: string;
  quantity: number;
  price: number; // Price at the time of purchase
}

// Defines the structure of a complete order
export interface Order {
  id: string;
  orderDate: string;
  status: 'Delivered' | 'Processing' | 'Shipped' | 'Cancelled';
  totalAmount: number;
  items: OrderItem[];
  trackingInfo?: TrackingInfo; // This is the new optional property
}
export interface TrackingInfo {
  provider: string; // e.g., 'Delhivery', 'BlueDart'
  status: string;
  location: string;
  trackingNumber: string;
  lastUpdated: string;
}


