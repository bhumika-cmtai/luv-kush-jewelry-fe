  _id: string
  slug: string
  name: string
  description?: string
  images: string[]
  video?: string
  stock: number
  originalPrice: number
  price: number
  type: string;
  gender: "women" | "men" | "unisex"
  tags?: string[]
  color?: string[]
  material?: string
  adminPackagingWeight?: string
  adminPackagingDimension?: string
  dimensions?: string
  stones?: string[]
  jewelleryCategory?: string
  materialType?: string
  size?: string[]

  // priceOriginal: number
  // priceDiscounted: number
  // category:string;
  // metal: string;
